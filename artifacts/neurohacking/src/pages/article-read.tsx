function renderBlock(block: string, i: number) {
  const lines = block.split('\n');
  const hasBullets = lines.some(l => l.startsWith('• ') || l.startsWith('✅ '));
  const isHeader = lines.length === 1 && block.length <= 80 && !block.startsWith('•') && !block.startsWith('✅');

  if (isHeader && i > 0) {
    return (
      <motion.p
        key={i}
        initial={{ opacity: 0, y: 8 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ delay: Math.min(i * 0.04, 0.6) }}
        style={{ fontSize: 16, fontWeight: 700, color: 'var(--text-blue)', marginTop: 8 }}
      >
        {block}
      </motion.p>
    );
  }

  if (hasBullets) {
    return (
      <motion.div
        key={i}
        initial={{ opacity: 0, y: 8 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ delay: Math.min(i * 0.04, 0.6) }}
        className="space-y-2"
      >
        {lines.map((line, j) => {
          if (line.startsWith('• ')) {
            return (
              <div key={j} className="flex items-start gap-2">
                <span style={{ color: 'var(--text-blue)', marginTop: 3, flexShrink: 0 }}>•</span>
                <p className="body text-primary leading-relaxed">{line.slice(2)}</p>
              </div>
            );
          }
          if (line.startsWith('✅ ')) {
            return (
              <div key={j} className="flex items-start gap-2">
                <span style={{ fontSize: 15, marginTop: 2, flexShrink: 0 }}>✅</span>
                <p className="body leading-relaxed" style={{ color: 'var(--text-blue)', fontWeight: 600 }}>{line.slice(3)}</p>
              </div>
            );
          }
          return line ? <p key={j} className="body text-primary leading-relaxed">{line}</p> : null;
        })}
      </motion.div>
    );
  }

  return (
    <motion.p
      key={i}
      initial={{ opacity: 0, y: 8 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ delay: Math.min(i * 0.04, 0.6) }}
      className="body text-primary leading-relaxed"
    >
      {block}
    </motion.p>
  );
}

export default function ArticleRead() {
  const { id } = useParams<{ id: string }>();
  const [, setLocation] = useLocation();
  const { readArticles, updateState } = useAppStore();

  const article = ARTICLES_DATA[id || ''];
  const alreadyRewarded = readArticles.includes(id || '');

  const handleFinish = () => {
    if (!alreadyRewarded) {
      const now = new Date().toISOString();
      updateState(prev => ({
        potential: Math.min(100, prev.potential + POTENTIAL_REWARD),
        readArticles: [...prev.readArticles, id || ''],
        potentialHistory: [
          { date: now, source: `Статья: ${article?.title ?? id}`, amount: POTENTIAL_REWARD },
          ...prev.potentialHistory,
        ],
        activityLog: [
          {
            id: `act_${Date.now()}`,
            date: now,
            type: 'article' as const,
            keysGained: 0,
            potentialGained: POTENTIAL_REWARD,
            details: { articleTitle: article?.title ?? id },
          },
          ...prev.activityLog,
        ],
      }));
    }
    setLocation('/academy');
  };

  if (!article) {
    return (
      <div className="flex flex-col h-[100dvh] items-center justify-center">
        <p className="body text-secondary">Статья не найдена</p>
        <button onClick={() => setLocation('/academy')} className="mt-4 text-blue-light body-s">
          В академию
        </button>
      </div>
    );
  }

  return (
    <div className="flex flex-col min-h-[100dvh]">
      <div
        className="fixed top-0 left-0 right-0 z-50 flex justify-center"
        style={{ background: 'rgba(15,32,53,0.96)', backdropFilter: 'blur(20px)', borderBottom: '1px solid rgba(255,255,255,0.05)' }}
      >
        <div className="w-full max-w-[390px] h-[48px] flex items-center px-4">
          <button onClick={() => setLocation('/academy')} className="p-1 text-primary mr-3 active:opacity-60">
            <ChevronLeft size={26} />
          </button>
          <span className="body-s text-primary truncate flex-1">{article.title}</span>
        </div>
      </div>

      <div className="pt-[60px] px-5 pb-[100px]">
        <motion.h1
          initial={{ opacity: 0, y: 12 }}
          animate={{ opacity: 1, y: 0 }}
          className="title-l text-primary mb-2 mt-4 leading-snug"
        >
          {article.title}
        </motion.h1>
        <span className="caption text-primary mb-8 block">{new Date().toLocaleDateString('ru-RU')}</span>

        <div className="space-y-4">
          {article.content.split('\n\n').map((block, i) => renderBlock(block, i))}
        </div>
      </div>

      <div
        className="fixed bottom-0 left-0 right-0 flex justify-center"
        style={{ background: 'rgba(15,32,53,0.97)', backdropFilter: 'blur(20px)', borderTop: '1px solid rgba(255,255,255,0.05)' }}
      >
        <div className="w-full max-w-[390px] px-4 py-4">
          <button
            onClick={handleFinish}
            className="btn-grad btn-shimmer w-full h-[52px] rounded-[14px] text-white title-s active:opacity-90"
          >
            {alreadyRewarded ? 'Готово' : 'Завершить чтение'}
          </button>
        </div>
      </div>
    </div>
  );
}
